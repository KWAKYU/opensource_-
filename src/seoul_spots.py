def get_spot_info(location: str):
    """
    location 문자열에서 알려진 상권 찾기
    반환: (x경도, y위도, radius) 또는 None
    """
    # 완전 일치 먼저
    if location in SEOUL_SPOTS:
        return SEOUL_SPOTS[location]
    # 부분 일치 (예: "성수동 카페" → 성수동)
    for spot, coords in SEOUL_SPOTS.items():
        if spot in location or location in spot:
            return coords
    return None
